    "def split_into_sentences(text):\n",
    "    \"\"\"Split abstract into clean sentences using spaCy.\"\"\"\n",
    "    doc = nlp(text)\n",
    "    sentences = []\n",
    "    for sent in doc.sents:\n",
    "        s = sent.text.strip()\n",
    "        # Apply sentence-level filters\n",
    "        if len(s) < CONFIG['min_sentence_length']:\n",
    "            continue\n",
    "        if len(s) > CONFIG['max_sentence_length']:\n",
    "            continue\n",
    "        if len(s.split()) < CONFIG['min_token_count']:\n",
    "            continue\n",
    "        # Skip sentences that are mostly numbers/symbols\n",
    "        alpha_ratio = sum(c.isalpha() for c in s) / len(s)\n",
    "        if alpha_ratio < 0.6:\n",
    "            continue\n",
    "        sentences.append(s)\n",
    "    return sentences\n",
    "\n",
    "logger.info('Splitting abstracts into sentences (this may take 1-2 minutes)...')\n",
    "\n",
    "all_sentences = []\n",
    "for idx, row in df.iterrows():\n",
    "    sentences = split_into_sentences(row['abstract'])\n",
    "    for sent in sentences:\n",
    "        all_sentences.append({\n",
    "            'sentence_id'   : f\"s{idx:04d}_{len(all_sentences):05d}\",\n",
    "            'sentence'      : sent,\n",
    "            'source_title'  : row['title'],\n",
    "            'year'          : row['year'],\n",
    "            'venue'         : row['venue'],\n",
    "            'citation_count': row['citation_count'],\n",
    "            'fields'        : row['fields'],\n",
    "        })\n",
    "\n",
    "df_sentences = pd.DataFrame(all_sentences)\n",
    "logger.info(f'Done! Total sentences extracted: {len(df_sentences)}')\n",
    "\n",
