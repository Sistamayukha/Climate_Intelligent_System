    "# Score sentences by climate richness for smarter sampling\n",
    "def score_sentence(text):\n",
    "    \"\"\"Score based on climate term density — higher = more useful for annotation.\"\"\"\n",
    "    text_lower = text.lower()\n",
    "    term_hits = sum(1 for term in CLIMATE_TERMS if term.lower() in text_lower)\n",
    "    return term_hits\n",
    "\n",
    "df_sentences['climate_score'] = df_sentences['sentence'].apply(score_sentence)\n",
    "\n",
    "# Keep only sentences with at least 1 climate term for annotation\n",
    "df_relevant = df_sentences[df_sentences['climate_score'] >= 1].copy()\n",
    "logger.info(f'Sentences with climate terms: {len(df_relevant)}')\n",
    "\n",
    "# Stratified sample by year for temporal diversity\n",
    "df_relevant['year_bucket'] = df_relevant['year'].apply(\n",
    "    lambda y: '2010-2014' if y <= 2014 else ('2015-2019' if y <= 2019 else '2020-2025')\n",
    ")\n",
    "\n",
    "sample_size = min(CONFIG['annotation_sample_size'], len(df_relevant))\n",
    "\n",
    "try:\n",
    "    # Stratified sample across year buckets\n",
    "    annotation_sample = df_relevant.groupby('year_bucket', group_keys=False).apply(\n",
    "        lambda x: x.sample(min(len(x), sample_size // 3), random_state=42)\n",
    "    ).sample(min(sample_size, len(df_relevant)), random_state=42)\n",
    "except:\n",
    "    annotation_sample = df_relevant.sample(sample_size, random_state=42)\n",
    "\n",
    "# Sort by score — best sentences first\n",
    "annotation_sample = annotation_sample.sort_values('climate_score', ascending=False)\n",
    "annotation_sample = annotation_sample.reset_index(drop=True)\n",
    "\n",
    "# Add annotation columns (empty — to be filled in Label Studio)\n",
    "annotation_sample['entities'] = ''  # Will be filled during annotation\n",
    "annotation_sample['relations'] = '' # Will be filled during annotation\n",
    "annotation_sample['annotated'] = False\n",
    "\n",
    "print(f'Annotation Sample Ready:')\n",
    "print(f'  Total sentences selected : {len(annotation_sample)}')\n",
    "print(f'\\nYear bucket distribution:')\n",
    "print(annotation_sample['year_bucket'].value_counts().to_string())\n",
    "print(f'\\nClimate score distribution:')\n",
    "print(annotation_sample['climate_score'].value_counts().sort_index(ascending=False).head(8).to_string())\n",
